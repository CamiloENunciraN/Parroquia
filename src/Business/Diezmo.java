public class Diezmo {
    private String cedula;
    private Double valorDiezmo;

    public Diezmo(String cedula, Double valorDiezmo) {
        this.cedula = cedula;
        this.valorDiezmo = valorDiezmo;
    }

    public String getCedula() {
        return cedula;
    }

    public void setCedula(String cedula) {
        this.cedula = cedula;
    }

    public Double getValorDiezmo() {
        return valorDiezmo;
    }

    public void setValorDiezmo(Double valorDiezmo) {
        this.valorDiezmo = valorDiezmo;
    }
    
    
    
    
    
}
